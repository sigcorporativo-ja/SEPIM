package es.juntadeandalucia.sepim.model;

import java.util.ArrayList;
import java.util.List;

import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.OrderBy;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

@Entity
@Table(schema = "public", name = "entities_categories")
public class EntityCategory {

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO, generator = "entities_categories_seq_gen")
	@SequenceGenerator(name = "entities_categories_seq_gen", sequenceName = "public.entities_categories_id_seq")
	protected Integer id;

	@OneToMany(cascade = { CascadeType.ALL }, mappedBy = "category", targetEntity = es.juntadeandalucia.sepim.model.Entity.class, orphanRemoval = true, fetch = FetchType.LAZY)
	// @LazyCollection(LazyCollectionOption.FALSE)
	@OrderBy("name")
	protected List<es.juntadeandalucia.sepim.model.Entity> entities = new ArrayList<es.juntadeandalucia.sepim.model.Entity>();

	/**
	 * Nombre asociado a la categoria
	 */
	protected String name;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<es.juntadeandalucia.sepim.model.Entity> getEntities() {
		return entities;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		EntityCategory other = (EntityCategory) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
}
