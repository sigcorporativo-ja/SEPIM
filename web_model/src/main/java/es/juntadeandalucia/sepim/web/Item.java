package es.juntadeandalucia.sepim.web;

import java.util.ArrayList;
import java.util.List;

/**
 * Clase utilizada para representar a un elemento recuperado de una consulta
 * 
 * @author Marco Antonio Fuentelsaz Perez
 * @since 1.0.0
 *
 */
public class Item {

	/**
	 * Envelope
	 */
	protected double minX, minY, maxX, maxY;
	
	/**
	 * Nombre asociado al elemento
	 */
	protected String name;
	
	protected double distance;
	
	protected String pkValue;

	/**
	 * Lista de campos
	 */
	protected List<ItemFieldValue> fields = new ArrayList<ItemFieldValue>();

	public List<ItemFieldValue> getFields() {
		return fields;
	}

	public double getMaxX() {
		return maxX;
	}

	public double getMaxY() {
		return maxY;
	}

	public double getMinX() {
		return minX;
	}

	public double getMinY() {
		return minY;
	}

	public String getName() {
		return name;
	}

	public void setMaxX(double maxX) {
		this.maxX = maxX;
	}

	public void setMaxY(double maxY) {
		this.maxY = maxY;
	}

	public void setMinX(double minX) {
		this.minX = minX;
	}

	public void setMinY(double minY) {
		this.minY = minY;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getDistance() {
		return distance;
	}

	public void setDistance(double distance) {
		this.distance = distance;
	}

	public String getPkValue() {
		return pkValue;
	}

	public void setPkValue(String pkValue) {
		this.pkValue = pkValue;
	}
}
